const { Groq } = require('groq-sdk');
const { PermissionFlagsBits } = require('discord.js');

const groq = new Groq({
    apiKey: process.env.GROQ_API_KEY
});

const aiCooldowns = new Set();

const moderationTools = [
    {
        type: "function",
        function: {
            name: "clear_messages",
            description: "Deletes a specific number of messages from the channel.",
            parameters: {
                type: "object",
                properties: {
                    amount: { type: "number", description: "The number of messages to clear (1-100)." }
                },
                required: ["amount"]
            }
        }
    },
    {
        type: "function",
        function: {
            name: "kick_member",
            description: "Kicks a member from the server.",
            parameters: {
                type: "object",
                properties: {
                    userId: { type: "string", description: "The mention or ID of the user to kick." },
                    reason: { type: "string", description: "The reason for the kick." }
                },
                required: ["userId"]
            }
        }
    },
    {
        type: "function",
        function: {
            name: "ban_member",
            description: "Bans a member from the server.",
            parameters: {
                type: "object",
                properties: {
                    userId: { type: "string", description: "The mention or ID of the user to ban." },
                    reason: { type: "string", description: "The reason for the ban." }
                },
                required: ["userId"]
            }
        }
    }
];

module.exports = (client) => {
    client.on('messageCreate', async (message) => {
        // Ignore other bots and empty messages (CRITICAL so the bot doesn't talk to itself forever)
        if (message.author.bot || !message.content) return;

        const text = message.content.toLowerCase();

        // ==========================================
        // 1. IMAGE GENERATOR (.imagine)
        // ==========================================
        if (text.startsWith('.imagine ')) {
            const imagePrompt = message.content.slice(9).trim();
            if (!imagePrompt) return message.channel.send('Please tell me what to draw!');

            const replyMsg = await message.channel.send('🎨 Painting your picture...');
            try {
                const safePrompt = encodeURIComponent(imagePrompt);
                const imageUrl = `https://image.pollinations.ai/prompt/${safePrompt}?width=1024&height=1024&nologo=true`;
                await message.channel.send({ content: `🖼️ **${imagePrompt}**\nGenerated by ${message.author}`, files: [imageUrl] });
                return await replyMsg.delete();
            } catch (error) {
                return replyMsg.edit('❌ Trouble drawing that.');
            }
        }

        // ==========================================
        // 2. OMNIPRESENT TEXT & MODERATION
        // ==========================================
        
        // ALL TRIGGERS REMOVED. Starry will now process every single message.

        // Silent Cooldown to prevent API spam without flooding the chat
        if (aiCooldowns.has(message.author.id)) return;

        aiCooldowns.add(message.author.id);
        // Wait 4 seconds before this user can trigger the bot again
        setTimeout(() => aiCooldowns.delete(message.author.id), 4000);

        await message.channel.sendTyping();

        try {
            const chatCompletion = await groq.chat.completions.create({
                messages: [
                    { role: "system", content: "You are Starry, a helpful, friendly Discord bot. You are currently in a public chat room reading everyone's messages. Keep your replies very short, conversational, and natural. You have access to moderation tools. If a user explicitly asks you to kick, ban, or clear messages, use the tool." },
                    { role: "user", content: `${message.author.username} says: ${message.content}` }
                ],
                model: "llama-3.1-8b-instant",
                tools: moderationTools,
                tool_choice: "auto"
            });

            const responseMessage = chatCompletion.choices[0].message;

            // Check if the AI decided to execute a moderation tool
            if (responseMessage.tool_calls) {
                const toolCall = responseMessage.tool_calls[0];
                const args = JSON.parse(toolCall.function.arguments);
                const functionName = toolCall.function.name;

                // 🛡️ SECURITY CHECK
                if (!message.member.permissions.has(PermissionFlagsBits.ManageMessages) && functionName === 'clear_messages') {
                    return message.channel.send(`❌ Sorry ${message.author.username}, you don't have permission to clear messages!`);
                }
                if (!message.member.permissions.has(PermissionFlagsBits.KickMembers) && functionName === 'kick_member') {
                    return message.channel.send(`❌ Sorry ${message.author.username}, you don't have permission to kick members!`);
                }
                if (!message.member.permissions.has(PermissionFlagsBits.BanMembers) && functionName === 'ban_member') {
                    return message.channel.send(`❌ Sorry ${message.author.username}, you don't have permission to ban members!`);
                }

                // Execute Tools
                if (functionName === "clear_messages") {
                    const amount = Math.min(args.amount, 100);
                    await message.channel.bulkDelete(amount + 1, true);
                    const modLog = await message.channel.send(`🧹 Cleared ${amount} messages!`);
                    setTimeout(() => modLog.delete().catch(() => {}), 4000);
                    return;
                }

                const targetId = args.userId.replace(/[<@!>]/g, '');
                const targetMember = await message.guild.members.fetch(targetId).catch(() => null);

                if (!targetMember) return message.channel.send("❌ I couldn't find that member in this server.");
                if (!targetMember.modifiable) return message.channel.send("❌ I cannot moderate this user. Their role is higher than mine!");

                if (functionName === "kick_member") {
                    await targetMember.kick(args.reason || "Kicked by Starry AI");
                    return message.channel.send(`✅ Successfully kicked **${targetMember.user.tag}**.`);
                }

                if (functionName === "ban_member") {
                    await targetMember.ban({ reason: args.reason || "Banned by Starry AI" });
                    return message.channel.send(`✅ Successfully banned **${targetMember.user.tag}**.`);
                }
            }

            // Standard text reply
            const replyText = responseMessage.content || "Hmm...";
            return message.channel.send(replyText.length > 2000 ? replyText.slice(0, 1995) + "..." : replyText);

        } catch (error) {
            console.error("Groq Error:", error.message);
            // Silently fail so it doesn't spam errors into the chat
            return;
        }
    });
};
            
